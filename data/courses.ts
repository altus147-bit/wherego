// Attach computed scores so list pages can sort consistently.
export const courses: Course[] = raw.map((c) => ({
  ...c,
  score: computeCourseScore({
    recommendCount: c.recommendCount,
    saveCount: c.saveCount,
    ratingAvg: c.ratingAvg,
    viewCount: c.viewCount,
    createdAt: c.createdAt,
  }),
}));

export function findCourse(id: string): Course | undefined {
  return courses.find((c) => c.id === id);
}

export function coursesByRegion(regionId: string): Course[] {
  return courses.filter((c) => c.regionId === regionId && c.status === 'approved');
}

export function topCourses(n = 5): Course[] {
  return [...courses]
    .filter((c) => c.status === 'approved')
    .sort((a, b) => b.score - a.score)
    .slice(0, n);
}
